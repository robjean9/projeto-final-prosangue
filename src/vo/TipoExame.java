package vo;

/**
 *
 * @author robson
 */
public class TipoExame {
    private Integer id;
    private String tipo;

    public TipoExame(Integer id, String tipo) {
        this.id = id;
        this.tipo = tipo;
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
     
    
    
}
